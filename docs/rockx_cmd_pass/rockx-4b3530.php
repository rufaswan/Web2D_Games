<?php

function rockx( $fname )
{
	$file = file_get_contents($fname);
	if ( empty($file) )  return;

	$base = 0x4b3530;
	for ( $i=0; $i < 12; $i++ )
	{
		$p0 = $base + ($i * 0x14);
		printf("%6x\n\t= ", $p0);

		$pass = '';
		for ( $j=0; $j < 8; $j++ )
		{
			$p1 = $p0 + ($j << 1);
			$b0 = ord($file[$p1+0]);
			$b1 = ord($file[$p1+1]);

			$v = ($b0 << 8) | $b1;
			$d = $v - 0x824f;
			if ( $d < 0 || $d > 10 )
				continue;

			printf('%4x ', $v);
			$pass .= $d;
		} // for ( $j=0; $j < 8; $j++ )

		printf("\n\t= %s\n", $pass);
	} // for ( $i=0; $i < 0x1116; $i++ )
	return;
}

